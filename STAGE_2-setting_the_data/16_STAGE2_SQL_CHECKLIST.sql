PROMPT ===== 5. FK LOGICAL ORPHAN CHECKS =====
SELECT 'COST_CENTERS -> ORG_UNITS' AS check_name, COUNT(*) AS orphan_rows
FROM   COST_CENTERS c
WHERE  c.ORG_UNIT_ID IS NOT NULL
AND    NOT EXISTS (
         SELECT 1
         FROM   ORG_UNITS o
         WHERE  o.ORG_UNIT_ID = c.ORG_UNIT_ID
       )
UNION ALL
SELECT 'ORG_UNITS parent -> ORG_UNITS', COUNT(*)
FROM   ORG_UNITS c
WHERE  c.PARENT_ORG_UNIT_ID IS NOT NULL
AND    NOT EXISTS (
         SELECT 1
         FROM   ORG_UNITS p
         WHERE  p.ORG_UNIT_ID = c.PARENT_ORG_UNIT_ID
       )
UNION ALL
SELECT 'BUDGET_ALLOCATIONS -> BUDGET_PERIODS', COUNT(*)
FROM   BUDGET_ALLOCATIONS b
WHERE  NOT EXISTS (
         SELECT 1
         FROM   BUDGET_PERIODS p
         WHERE  p.PERIOD_ID = b.PERIOD_ID
       )
UNION ALL
SELECT 'BUDGET_ALLOCATIONS -> COST_CENTERS', COUNT(*)
FROM   BUDGET_ALLOCATIONS b
WHERE  NOT EXISTS (
         SELECT 1
         FROM   COST_CENTERS c
         WHERE  c.COST_CENTER_ID = b.COST_CENTER_ID
       )
UNION ALL
SELECT 'BUDGET_ALLOCATIONS -> EXPENSE_CATEGORIES', COUNT(*)
