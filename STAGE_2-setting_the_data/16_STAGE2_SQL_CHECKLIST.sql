PROMPT ===== 4. UNIQUE BUSINESS KEY CHECKS =====
SELECT 'ORG_UNITS.ORG_UNIT_CODE duplicates' AS check_name, COUNT(*) AS duplicate_groups
FROM (
  SELECT ORG_UNIT_CODE
  FROM   ORG_UNITS
  GROUP  BY ORG_UNIT_CODE
  HAVING COUNT(*) > 1
)
UNION ALL
SELECT 'COST_CENTERS.COST_CENTER_CODE duplicates', COUNT(*)
FROM (
  SELECT COST_CENTER_CODE
  FROM   COST_CENTERS
  GROUP  BY COST_CENTER_CODE
  HAVING COUNT(*) > 1
)
UNION ALL
SELECT 'BUDGET_PERIODS.PERIOD_CODE duplicates', COUNT(*)
FROM (
  SELECT PERIOD_CODE
  FROM   BUDGET_PERIODS
  GROUP  BY PERIOD_CODE
  HAVING COUNT(*) > 1
)
UNION ALL
SELECT 'EXPENSE_CATEGORIES.CATEGORY_CODE duplicates', COUNT(*)
FROM (
  SELECT CATEGORY_CODE
  FROM   EXPENSE_CATEGORIES
  GROUP  BY CATEGORY_CODE
  HAVING COUNT(*) > 1
)
UNION ALL
SELECT 'BUDGET_ALLOCATIONS business grain duplicates', COUNT(*)
FROM (
  SELECT PERIOD_ID, COST_CENTER_ID, EXPENSE_CATEGORY_ID, BUDGET_VERSION
  FROM   BUDGET_ALLOCATIONS
  GROUP  BY PERIOD_ID, COST_CENTER_ID, EXPENSE_CATEGORY_ID, BUDGET_VERSION
  HAVING COUNT(*) > 1
)
ORDER BY 1;

